#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(scriptDir, '..', '..', '..');
const designPackRoot = path.join(root, 'packs', 'design');
const outPath = path.join(designPackRoot, 'DESIGN-CATALOG.md');

function readText(filePath) {
  return fs.readFileSync(filePath, 'utf8');
}

function listDirs(absPath) {
  if (!fs.existsSync(absPath)) return [];
  return fs
    .readdirSync(absPath, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort((a, b) => a.localeCompare(b));
}

function parseFrontmatter(filePath) {
  if (!fs.existsSync(filePath)) return {};
  const text = readText(filePath);
  const match = text.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return {};

  const body = match[1];
  const lines = body.split('\n');
  const data = {};

  for (let i = 0; i < lines.length; i += 1) {
    const line = lines[i];
    const m = line.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
    if (!m) continue;

    const key = m[1];
    const value = m[2] || '';

    if (value === '|') {
      const chunks = [];
      let j = i + 1;
      while (j < lines.length) {
        const next = lines[j];
        if (/^[A-Za-z0-9_-]+:\s*/.test(next)) break;
        if (!next.startsWith('  ')) break;
        chunks.push(next.slice(2).trim());
        j += 1;
      }
      data[key] = chunks.filter(Boolean).join(' ').trim();
      i = j - 1;
      continue;
    }

    data[key] = value.trim().replace(/^"|"$/g, '');
  }

  return data;
}

function chunk(items, size) {
  const rows = [];
  for (let i = 0; i < items.length; i += size) {
    rows.push(items.slice(i, i + size));
  }
  return rows;
}

const designSystemsDir = path.join(designPackRoot, 'design-systems');
const designSkillsDir = path.join(designPackRoot, 'design-skills');
const workflowSkillsDir = path.join(designPackRoot, 'runtime', 'skills', 'design');

const designSystems = listDirs(designSystemsDir);
const designSkills = listDirs(designSkillsDir);
const workflowSkills = listDirs(workflowSkillsDir);

const aestheticSkills = designSkills.filter((name) => name !== 'index.json');

const aestheticRows = aestheticSkills.map((slug) => {
  const skillPath = path.join(designSkillsDir, slug, 'SKILL.md');
  const fm = parseFrontmatter(skillPath);
  return {
    slug,
    description: fm.description || 'No description provided.'
  };
});

const workflowRows = workflowSkills.map((slug) => {
  const skillPath = path.join(workflowSkillsDir, slug, 'SKILL.md');
  const fm = parseFrontmatter(skillPath);
  return {
    slug,
    description: fm.description || 'No description provided.'
  };
});

const lines = [];
lines.push('# Design Content Pack Catalog');
lines.push('');
lines.push('This file is auto-generated by packs/design/scripts/generate-design-catalog.mjs.');
lines.push('Do not edit this file manually.');
lines.push('');
lines.push(`Generated: ${new Date().toISOString()}`);
lines.push('');
lines.push('This catalog belongs to the design content pack, not the stack-core runtime.');
lines.push(`Catalog location: ${path.relative(root, outPath)}`);
lines.push('Legacy stack-core paths remain compatibility shims where required.');
lines.push('');
lines.push('## Inventory Summary');
lines.push('');
lines.push(`- Design systems: ${designSystems.length}`);
lines.push(`- Aesthetic design skills: ${aestheticSkills.length}`);
lines.push(`- Workflow skills: ${workflowSkills.length}`);
lines.push('');

lines.push('## Design Systems');
lines.push('');
lines.push(`Source directory: ${path.relative(root, designSystemsDir)}`);
lines.push('');
for (const row of chunk(designSystems, 10)) {
  lines.push(row.map((x) => `- ${x}`).join('\n'));
  lines.push('');
}

lines.push('## Aesthetic Design Skills');
lines.push('');
lines.push(`Source directory: ${path.relative(root, designSkillsDir)}`);
lines.push('');
lines.push('| Slug | Description |');
lines.push('|---|---|');
for (const row of aestheticRows) {
  const description = row.description.replace(/\|/g, '\\|');
  lines.push(`| ${row.slug} | ${description} |`);
}
lines.push('');

lines.push('## Workflow Skills');
lines.push('');
lines.push(`Source directory: ${path.relative(root, workflowSkillsDir)}`);
lines.push('');
lines.push('| Skill | Description |');
lines.push('|---|---|');
for (const row of workflowRows) {
  const description = row.description.replace(/\|/g, '\\|');
  lines.push(`| ${row.slug} | ${description} |`);
}
lines.push('');

lines.push('## Stack-Core Consumption');
lines.push('');
lines.push('1. Read design system tokens from packs/design/design-systems/<brand>/DESIGN.md.');
lines.push('2. Optionally apply aesthetic rules from packs/design/design-skills/<style>/SKILL.md.');
lines.push('3. Build with design-pack workflow structure from packs/design/runtime/skills/design/<skill>/SKILL.md and assets/template.html.');
lines.push('4. Legacy `runtime/skills/design/` remains a compatibility shim for older consumers.');
lines.push('');

fs.writeFileSync(outPath, `${lines.join('\n')}\n`, 'utf8');
console.log(`Wrote ${path.relative(root, outPath)} with ${designSystems.length} systems, ${aestheticSkills.length} aesthetic skills, and ${workflowSkills.length} workflow skills.`);
